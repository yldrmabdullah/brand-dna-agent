"""Self-evaluation module for agent quality assurance."""

from typing import Any
from brand_dna.core.models import BrandDNADossier
from brand_dna.llm.client import LLMClient

async def self_evaluate(
    dossier: BrandDNADossier,
    llm: LLMClient,
    model: str
) -> dict[str, Any]:
    """Asks the LLM to critique its own dossier for consistency and grounding."""
    
    prompt = f"""
    ### BRAND INTELLIGENCE SELF-EVALUATION
    
    You are an AI Quality Auditor. You are reviewing a 'Brand DNA Dossier' generated by another AI agent.
    
    BRAND: {dossier.brand_name}
    POSITIONING: {dossier.positioning}
    SUMMARY: {dossier.executive_summary}
    
    CRITIQUE CRITERIA:
    1. GROUNDING: Does the positioning align with the visual clusters and audience data?
    2. CONSISTENCY: Are there contradictions between the 'Brand Voice' and 'Audience'?
    3. SPECIFICITY: Is the analysis too generic (e.g., 'high quality fabrics') or specific (e.g., 'distressed 12oz denim')?
    4. HALLUCINATION: Does anything feel made up or based on assumptions not present in the data?
    
    Output a JSON with:
    - score: (0-100)
    - findings: list of strings
    - risks: list of strings
    - recommendation: string
    """
    
    try:
        response = await llm.complete(
            prompt=prompt,
            model=model,
            system_prompt="You are a strict technical auditor for fashion intelligence.",
            json_mode=True
        )
        return response.json
    except Exception:
        return {
            "score": 0,
            "findings": ["Evaluation failed"],
            "risks": ["System error"],
            "recommendation": "Manual review required"
        }
